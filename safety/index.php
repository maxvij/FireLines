									<p><span>Implement external security</span>In order to stop people from setting your
										property on fire, you need to make sure that they attract attention when they
										are planning to do things you don’t want them to do. For example, illuminate
										entrances, use motion-activated lighting near doors and windows, and keep trees
										trimmed so the building can be observed by passing patrols. This might scare off
										people with bad intention.</p>
								</div>
