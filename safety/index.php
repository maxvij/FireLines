						<h2>Playing with fire</h2>
					</div>
					<div class="content">
                        <div class="row">
                            <div class="col-12">
						        <p>Kids playing with fire without knowing the dangers of it still causes a significant amount of fires in the Netherlands nowadays. It is of course very difficult to teach young children not to play with fire, but there are some things that can be done to prevent them causing damage.</p>
                            </div>
                        </div>
                        <div class="row">
							<div class="col-5 col-sm-5 col-md-2">
								<img class="icon" src="../src/img/playing-1.svg"/>
							</div>
							<div class="col-7 col-sm-7 col-md-10">
								<p><span>Keep matches and lighters in a place which a child can not reach.</span></p>
							</div>
						</div>
						<div class="row">
							<div class="col-5 col-sm-5 col-md-2">
								<img class="icon" src="../src/img/playing-2.svg"/>
							</div>
							<div class="col-7 col-sm-7 col-md-10">
								<p><span>Use a childproof lighter</span>
									A childproof lighter has a mechanism in which two operations are required before the lighter burns. For a child up to four years this is very difficult to do.</p>
							</div>
						</div>
						<div class="row">
							<div class="col-5 col-sm-5 col-md-2">
								<img class="icon" src="../src/img/playing-4.svg"/>
							</div>
							<div class="col-7 col-sm-7 col-md-10">
								<p><span>Teach a child of two years old that he or she should not be near fire and that he or she may not touch burning candles</span>
									(Children under two years can not remember this information yet.)</p>
							</div>
						</div>
						<div class="row">
							<div class="col-5 col-sm-5 col-md-2">
								<img class="icon" src="../src/img/playing-5.svg"/>
							</div>
							<div class="col-7 col-sm-7 col-md-10">
								<p><span>Keep children away from the fireplace by shielding the fireplace with a fireplace gate</span>
									This is safer than a spark shield because a fireplace gate is anchored to the wall and can not fall over.</p>
							</div>
						</div>
                        <div class="row">
                            <div class="col-5 col-sm-5 col-md-2">
                                <img class="icon" src="../src/img/playing-3.svg"/>
                            </div>
                            <div class="col-7 col-sm-7 col-md-10">
                                <p><span>Do not burn candles if there are children nearby</span>
                                    If you decide to do light candles, put them out of range and preferably use short, wide candles.</p>
                            </div>
                        </div>
						<div class="row">
							<div class="col-5 col-sm-5 col-md-2">
								<img class="icon" src="../src/img/playing-6.svg"/>
							</div>
							<div class="col-7 col-sm-7 col-md-10">
								<p><span>Playfully teach children that things like ovens or frying pans are hot</span>
									For example, when you open the oven: jump backwards, look shocked and say: "Ohno, that’s HOT!".<br>Children tend to imitate you and will therefor playfully learn that the oven should not be touched.
								</p>
							</div>
