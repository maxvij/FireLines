								<div class="col-5 col-sm-5 col-md-2">
									<img class="icon" src="../src/img/arson-4.svg"/>
								</div>
								<div class="col-7 col-sm-7 col-md-10">
									<p><span>Equip your home/office with fire sprinklers</span>
										In case it is too late to prevent a fire, it is important that the fire causes
										minimal damage. This can be achieved by installing fire sprinklers, which will
										discharges water when a fire is detected.</p>
								</div>
							</div>
