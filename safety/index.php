								<div class="col-7 col-sm-7 col-md-10">
									<p><span>Perform a risk assessment</span>Piles of trash, rubbish, or recyclables
										leave your home particularly vulnerable to an arsonist providing an accessible
										fuel for a fire. Please keep this in mind and put trash out of sight.</p>
								</div>
							</div>
							<div class="row">
								<div class="col-5 col-sm-5 col-md-2">
									<img class="icon" src="../src/img/arson-2.svg"/>
